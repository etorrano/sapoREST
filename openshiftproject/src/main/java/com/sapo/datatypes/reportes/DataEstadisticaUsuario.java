package com.sapo.datatypes.reportes;

public class DataEstadisticaUsuario {
	private Long cantidad_almacenes;
	private Long cantidad_colaboradores;
	private Long cantidad_notificaciones;
	
	public Long getCantidad_almacenes() {
		return cantidad_almacenes;
	}
	public void setCantidad_almacenes(Long cantidad_almacenes) {
		this.cantidad_almacenes = cantidad_almacenes;
	}
	public Long getCantidad_colaboradores() {
		return cantidad_colaboradores;
	}
	public void setCantidad_colaboradores(Long cantidad_colaboradores) {
		this.cantidad_colaboradores = cantidad_colaboradores;
	}
	public Long getCantidad_notificaciones() {
		return cantidad_notificaciones;
	}
	public void setCantidad_notificaciones(Long cantidad_notificaciones) {
		this.cantidad_notificaciones = cantidad_notificaciones;
	}
	
}
